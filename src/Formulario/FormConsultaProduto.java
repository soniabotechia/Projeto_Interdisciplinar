    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
               
        String nome = txtConsulta.getText();
             
        ProdutoBO produtoBO = new ProdutoBO();
        List<Produto> produtos = produtoBO.pesquisa(nome);
        JtConsultaProdutos.setModel(new ProdutoTableModel(produtos));
        JtConsultaProdutos.repaint();
    
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void JtConsultaProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_JtConsultaProdutosMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2) {
            int row = JtConsultaProdutos.rowAtPoint(evt.getPoint());
           Produto produto = ((ProdutoTableModel) JtConsultaProdutos.getModel()).getValueAt(row);
           
           if(callbackProduto != null) {
               callbackProduto.fim(produto);
           }
            
        }
        
    }//GEN-LAST:event_JtConsultaProdutosMouseClicked


